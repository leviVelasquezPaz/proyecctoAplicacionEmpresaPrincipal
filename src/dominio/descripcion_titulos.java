package dominio;

/**
 *
 * @author Levi
 */
public class descripcion_titulos {

    private int id;
    private String titulo;

    public descripcion_titulos(int id, String titulo) {
        this.id = id;
        this.titulo = titulo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    @Override
    public String toString() {
        return "descripcion_titulos{" + "id=" + id + ", titulo=" + titulo + '}';
    }

}
